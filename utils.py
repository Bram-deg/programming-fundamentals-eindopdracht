# utility functions for the project

def print_divider():
    """Prints a divider made of asterisks.
    Used to separate different sections of the program."""
    print("*" * 50)

def print_main_menu():
    """Prints the main menu options for the Pokémon App."""
    print_divider()
    print("1. Pokédex")
    print("2. Who's that Pokémon?")
    print("3. Pokémon Type Trainer")
    print("4. Exit")
    print_divider()